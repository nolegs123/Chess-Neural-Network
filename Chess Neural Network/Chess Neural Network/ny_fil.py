message = input("Write the word 'hello' in your native language").lower()
if message == "hej":
    print("HEJ")
elif message == "hello":
    print("HELLO")
elif message == "hola":
    print("HOLA")
elif message == "aloha":
    print("ALOHA")
elif message == "bonjour":
    print("BONJOUR")
elif message == "hallo":
    print("HALLO")
elif message == "witaf":
    print("WITAF")
else:
    print("BRUH MOMEMENT")